        String qty =  jTextField1.getText();
        String price = jTextField2.getText();
        String total = jLabel7.getText();
        String bill = jLabel9.getText();
        
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        table.addRow(new Object[] {item, qty, price, total});
        double totalValue = Double.parseDouble(jLabel7.getText());
        double billValue = Double.parseDouble(jLabel9.getText());
