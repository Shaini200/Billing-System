    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        String total = jTable1.getValueAt(row, 3).toString();
        double billValue = Double.parseDouble(jLabel9.getText());
        double totalValue = Double.parseDouble(total);
        double billAmount = billValue - totalValue;
        jLabel9.setText(String.valueOf(billAmount));
        table.removeRow(row);
        JOptionPane.showMessageDialog(this, "Item Removed Successfully", "Done", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

